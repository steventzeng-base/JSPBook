package tw.com.javaworld.CH8;

public class SimpleBean {

    private String name;
    private String number;
    public SimpleBean() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
}
